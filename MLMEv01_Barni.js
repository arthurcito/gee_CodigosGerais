//-------3 - Script do MLME-------//
var l8Bandas = ee.Image('LANDSAT/LC08/C02/T1_L2/LC08_231060_20231120')
    .select('SR_B2', 'SR_B3', 'SR_B4', 'SR_B5', 'SR_B6');

var veg = l8Bandas.reduceRegion(ee.Reducer.mean(), veg_em, 30).values();
